// complex pattern****************************************

int n;
cin>>n;
int i=1;
while(i<=n)
{
    int space =n-i;
    while(space>0)
    {
        cout<<" ";
        space--;

    }
    int j=1;
    int s=1;
    while(j<=i)
    {
        cout<<s;
        s++;
        j++;

    }
    int k=i-1;
    while(k>0)
    {
        cout<<k;
        k--;
    }
    int side=n-i;
    while(side>0)
    {
        cout<<" ";
        side--;
    }
    cout<<endl;
    i++;
}

